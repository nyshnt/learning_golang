// Using the following operators, write expressions and assign their values to variables:
// ==
// <=
// >=
// !=
// <
// >

package main

import "fmt"

func main() {

	g := (14 == 14)
	h := (15 <= 15)
	i := (16 >= 16)
	j := (17 != 17)
	k := (18 < 19)
	l := (19 > 20)

	fmt.Println(g, h, i, j, k, l)
}
